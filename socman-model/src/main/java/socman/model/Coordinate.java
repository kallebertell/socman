package socman.model;

/**
 * Represents an X, Y coordinate.
 * Use valueOf to create instances
 */
public class Coordinate {
	private Integer x;
	private Integer y;

	private Coordinate(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public static Coordinate valueOf(int x, int y) {
		return new Coordinate(x, y);
	}
	
	/** More sensible name for static imports */
	public static Coordinate coord(int x, int y) {
		return valueOf(x, y);
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((x == null) ? 0 : x.hashCode());
		result = prime * result + ((y == null) ? 0 : y.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Coordinate other = (Coordinate) obj;
		if (x == null) {
			if (other.x != null)
				return false;
		} else if (!x.equals(other.x))
			return false;
		if (y == null) {
			if (other.y != null)
				return false;
		} else if (!y.equals(other.y))
			return false;
		return true;
	}
	
	public String toString() {
		return "("+x+", "+y+")";
	}
}